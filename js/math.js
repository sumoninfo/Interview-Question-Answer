// Javascript max() function for 3 numbers

// ============= Get the max value from array=============
console.log(Math.max(1, 2, 3, 4, 5, 6)); //  6

let array = [1, 2, 3, 4, 5, 6, 10, 7];
let max   = Math.max(...array);
console.log(max); // 10

// ============= Get the min value from array=============
console.log(Math.min(1, 2, 3, 4, 5, 6)); //  1

let array2 = [1, 2, 3, 4, 5, 6, 10, 7];
let max2   = Math.max(...array);
console.log(max2); // 1
